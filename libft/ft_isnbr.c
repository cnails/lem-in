#include "libft.h"

int				ft_isnbr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
		{
			if (!(!i && str[i] != '-'))
				return (0);
		}
		i++;
	}
	return (1);
}
